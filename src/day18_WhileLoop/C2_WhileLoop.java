package day18_WhileLoop;

public class C2_WhileLoop {

	public static void main(String[] args) {
		
		
		for(int i=0;i<=30;i++ ) {
			if(i%4==0) {
				System.out.println(i+"");
			}
		}
		System.out.print(" ");
		
		int sayi=10;
	while(sayi<=30) {
		sayi++;
		if(sayi%4==0) {
			System.out.println(sayi+"");
	}
		
		}
		System.out.print(sayi+" ");
	}
	
	}
	
	


